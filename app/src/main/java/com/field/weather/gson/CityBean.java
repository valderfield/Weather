package com.field.weather.gson;
//接收gson 的城市类 返回的是 list<CityBean>
public class CityBean {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
